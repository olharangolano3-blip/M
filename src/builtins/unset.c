#include "parser.h"

int	builtin_unset(t_cmd *cmd, t_shell *shell)
{
	int	i;

	i = 1;
	while (cmd->argv[i])
	{
		env_unset(shell, cmd->argv[i]);
		i++;
	}
	return (0);
}
